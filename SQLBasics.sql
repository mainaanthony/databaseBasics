CREATE TABLE AUTHORS (

AUTHORID INT PRIMARY KEY,
FirstName VARCHAR(255),
LastName VARCHAR (255)
);


CREATE TABLE BOOKS(
BookID INT PRIMARY KEY,
TITLE VARCHAR(255),
AUTHORID INT,
FOREIGN KEY (AUTHORID) REFERENCES AUTHORS

);

